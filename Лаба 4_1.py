a = int(input("Введите длину прямоугольника: ")) # Ввод длины прямоугольника 
b = int(input("Введите ширину прямоугольника: ")) # Ввод ширины прямоугольника 

P = 2 * (a + b) # Вычисление периметра 
S = a * b # Вычисление площади 

print("Периметр прямоугольника:", P) # Вывод периметра 
print("Площадь прямоугольника:", S) # Вывод площади